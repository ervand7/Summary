a = [1, [2, [56, [44, 56, [67, 7878], 66], 78], 89, 45], [345, [5, 456, [343, 56], 2, 7, 9, 65, 234], 0, 23]]


def nested_rec(my_list, level=1):
    print(*my_list, ' |level=', level)
    for i in my_list:
        if type(i) == list:
            nested_rec(i, level + 1)


nested_rec(a)
