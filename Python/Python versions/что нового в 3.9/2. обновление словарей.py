# Обновление через |=
dict1 = {'a': 1, 'b': 2, 'c': 3}
dict2 = {'c': 100, 'd': 200}
dict1 |= dict2
print(dict1)  # {'a': 1, 'b': 2, 'c': 100, 'd': 200}


# Обновить словать теперь можно с помощью итерируемого объекта, например генератора:
a = {'a': 'one', 'b': 'two'}
b = ((i, i ** 2) for i in range(3))
a |= b
print(a)  # {'a': 'one', 'b': 'two', 0: 0, 1: 1, 2: 4}

